    private boolean donateFormCheck() {
    if (this.txtDonateName.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(this, "Please Enter Your Name");
      this.txtDonateName.grabFocus();
      return false;
    } 
    if (this.txtDonateName.getText().trim().length() > 30) {
      JOptionPane.showMessageDialog(this, "1. Name:\n\nMore Than 30 Characters Are Not Allowed!");
      this.txtDonateName.grabFocus();
      return false;
    } 
    if (this.cmbDonateGender.getSelectedIndex() == 0) {
      JOptionPane.showMessageDialog(this, "Please Select Your Gender");
      this.cmbDonateGender.grabFocus();
      return false;
    } 
    if (this.cmbDonateAge.getSelectedIndex() == 0) {
      JOptionPane.showMessageDialog(this, "Please Select Your Age");
      this.cmbDonateAge.grabFocus();
      return false;
    } 
    if (this.cmbDonateBlood.getSelectedIndex() == 0) {
      JOptionPane.showMessageDialog(this, "Please Select Your Blood Group");
      this.cmbDonateBlood.grabFocus();
      return false;
    } 
    if (this.cmbDonateHospital.getSelectedIndex() == 0) {
      JOptionPane.showMessageDialog(this, "Please Select A Nearest Hospital");
      this.cmbDonateHospital.grabFocus();
      return false;
    } 
    if (this.txtDonatePhone.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(this, "Please Enter Your Phone Number");
      this.txtDonatePhone.grabFocus();
      return false;
    } 
    if (this.txtDonateEmail.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(this, "Please Enter Your Email Address");
      this.txtDonateEmail.grabFocus();
      return false;
    } 
    if (this.txtDonateEmail.getText().trim().length() > 30) {
      JOptionPane.showMessageDialog(this, "7. Email:\n\nMore Than 30 Characters Are Not Allowed!");
      this.txtDonateEmail.grabFocus();
      return false;
    } 
    if (this.taDonateAddress.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(this, "Please Enter Your Address");
      this.taDonateAddress.grabFocus();
      return false;
    } 
    if (this.taDonateAddress.getText().trim().length() > 30) {
      JOptionPane.showMessageDialog(this, "8. Address:\n\nMore Than 30 Characters Are Not Allowed!");
      this.taDonateAddress.grabFocus();
      return false;
    } 
    if (this.cmbDonateDisease.getSelectedIndex() == 0) {
      JOptionPane.showMessageDialog(this, "Please Select Your Disease");
      this.cmbDonateDisease.grabFocus();
      return false;
    } 
    if (this.cmbDonateDisease.getSelectedIndex() == 1) {
      if (this.taDonateDisease.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please Explain Your Disease");
        this.taDonateDisease.grabFocus();
        return false;
      } 
      if (this.taDonateDisease.getText().trim().length() > 30) {
        JOptionPane.showMessageDialog(this, "10. Disease:\n\nMore Than 30 Characters Are Not Allowed!");
        this.taDonateDisease.grabFocus();
        return false;
      } 
    } 
    if (this.txtDonatePassword.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(this, "Please Enter Your New Password");
      this.txtDonatePassword.grabFocus();
      return false;
    } 
    if (this.txtDonateReEnterPassword.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(this, "Please Re-Enter Your New Password");
      this.txtDonateReEnterPassword.grabFocus();
      return false;
    } 
    if (!this.txtDonatePassword.getText().trim().equals(this.txtDonateReEnterPassword.getText())) {
      JOptionPane.showMessageDialog(this, "Both Passwords Not Match");
      this.txtDonatePassword.grabFocus();
      return false;
    } 
    if (!this.txtDonatePassword.getText().trim().isEmpty() && 
      this.txtDonatePassword.getText().trim().length() > 20) {
      JOptionPane.showMessageDialog(this, "12. Password:\n13. Re-Enter Password\n\nMore Than 20 Characters Are Not Allowed!");
      this.txtDonatePassword.grabFocus();
      return false;
    } 
    return true;
  }
    
    private boolean donateFormValidate() {
    String phoneNumber = this.txtDonatePhone.getText();
    String regex = "(0/91)?[6-9][0-9]{9}";
    boolean x = phoneNumber.matches(regex);
    if (!x) {
      JOptionPane.showMessageDialog(this, "Enter Valid Phone Number");
      this.txtDonatePhone.grabFocus();
      return false;
    } 
    String email = this.txtDonateEmail.getText();
    boolean y = isValid(email);
    if (!y) {
      JOptionPane.showMessageDialog(this, "Enter Valid Email Address");
      this.txtDonateEmail.grabFocus();
      return false;
    } 
    return (x == true && y == true);
  }
    
    private static boolean isValid(String email) {
    String regex = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
    return email.matches(regex);
     }
    
